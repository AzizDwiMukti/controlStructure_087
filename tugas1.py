# Evaluasi Kinerja Siswa
persentase = float(input("Masukkan persentase siswa: "))

if persentase >= 90:
    print("Kinerja Sangat Baik")
elif persentase >= 80:
    print("Kinerja Sangat Bagus")
elif persentase >= 70:
    print("Kinerja Baik")
elif persentase >= 60:
    print("Kinerja Rata-rata")
else:
    print("Perlu Peningkatan")

    #Program ini mengevaluasi kinerja siswa berdasarkan persentase yang diinput, dengan hasil sebagai berikut:

#≥ 90: Kinerja Sangat Baik
#≥ 80: Kinerja Sangat Bagus
#≥ 70: Kinerja Baik
#≥ 60: Kinerja Rata-rata
#< 60: Perlu Peningkatan


